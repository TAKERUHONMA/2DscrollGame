void Field::Draw()
{
	int scroll = 0;
	Camera* cam = GetParent()->FindGameObject<Camera>();
	if (cam != nullptr)
	{
		scroll = cam->GetValue();
	}

	for (int y = 0; y < HEIGHT; y++)
	{
		for (int x = 0; x < WIDTH; x++)
		{
			int chip = Map[y][x];
			DrawRectGraph(x * 32 - scroll, y * 32, 32 * (chip % 16), 32 * (chip / 16), 32, 32, hImage, TRUE);
		}
	}
}

int Field::CollisionRight(int x, int y)
{
	if (IsWallBlock(x, y))
	{
		//当たっているので、めり込んだ量を返す
		return x % 32 + 1;
	}
	return 0;
}

int Field::CollisionDown(int x, int y)
{
	if (IsWallBlock(x, y))
	{
		//当たっているので、めり込んだ量を返す
		return y % 32 + 1;
	}
	return 0;
}

int Field::CollisionUp(int x, int y)
{
	if (IsWallBlock(x, y))
	{
		return y % 32 - 1;
	}
	return 0;
}

int Field::CollisionLeft(int x, int y)
{
	if (IsWallBlock(x, y))
	{
		return x % 32 + 1;
	}
	return 0;
}

void Field::Reset()
{
	CsvReader csv;//データを読むクラスのインスタンスを作成
	for (int h = 0; h < height; h++) {
		for (int w = 0; w < width; w++) {
			switch (csv.GetInt(w, h + height + 1))
			{
			case 0://player
			{
				Player* pplayer = GetParent()->FindGameObject<Player>();
				pplayer->SetPosition(w * 32, h * 32);
			}
			break;
			case 1://bird
			{
				Bird* pbird = Instantiate<Bird>(GetParent());
				pbird->SetPosition(w * 32, h * 32);
			}
			break;
			}
		}
	}
}

